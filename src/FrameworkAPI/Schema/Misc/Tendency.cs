namespace FrameworkAPI.Schema.Misc;

/// <summary>
/// The current tendency of the value of one variable.
/// </summary>
public enum Tendency
{
    /// <summary>
    /// The value of the variable is currently increasing.
    /// </summary>
    Increasing,

    /// <summary>
    /// The value of the variable is currently steady.
    /// </summary>
    Steady,

    /// <summary>
    /// The value of the variable is currently decreasing.
    /// </summary>
    Decreasing
}